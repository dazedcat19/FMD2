function Init()
	local m = NewWebsiteModule()
	m.ID                       = 'dfd28538fd9e4c3b8786e0193a0f2984'
	m.Name                     = 'Manhwa68'
	m.RootURL                  = 'https://manhwa68.com'
	m.Category                 = 'H-Sites'
	m.OnGetNameAndLink         = 'GetNameAndLink'
	m.OnGetInfo                = 'GetInfo'
	m.OnGetPageNumber          = 'GetPageNumber'
end

